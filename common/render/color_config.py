class ColorManager:
    def __init__(self):
        '''constructor'''
        self.color_mapping = {}

        body_parts = ANNOTATION_CONFIG['bodyPart']
        for part_config in body_parts.values():
            part_config = part_config['annotationParts']

            for configs in part_config:
                plane_type = configs['name']
                self.color_mapping[plane_type] = self.parse_color_mapping(configs['config'])

    @classmethod
    def parse_color_mapping(cls, configs):
        color_mapping = {}
        for config in configs:
            structure_name = config['name']
            alias = config['alias'] if 'alias' in config else ''
            color = config['color'].split(',')
            color = [int(x) for x in color]

            color_mapping[structure_name] = {
                'color': color,
                'alias': alias
            }
        return color_mapping

    def get_color(self, plane_name, structure_name):
        if plane_name in self.color_mapping:
            color_mapping = self.color_mapping[plane_name]

            if structure_name in color_mapping:
                # print("getcolor",plane_name,color_mapping[structure_name]['color'])
                return color_mapping[structure_name]['color']

        return [255, 255, 0, 255]

    def get_alias(self, plane_name, structure_name):
        if plane_name in self.color_mapping:
            color_mapping = self.color_mapping[plane_name]

            if structure_name in color_mapping:
                return color_mapping[structure_name]['alias']

        return ''


